package rise.lib.config;

/**
 * Mongo Database Configuration
 * @author p.campanella
 *
 */
public class MongoConfig {
	
	/**
	 * Server Address
	 */
	public String address;
	
	/**
	 * Server Port
	 */
	public int port;
	
	/**
	 * Database name
	 */
	public String dbName;
	
	/**
	 * Db user
	 */
	public String user;
	
	/**
	 * Db Password
	 */
	public String password;
	
	/**
	 * Replica Name
	 */
	public String replicaName;
}
